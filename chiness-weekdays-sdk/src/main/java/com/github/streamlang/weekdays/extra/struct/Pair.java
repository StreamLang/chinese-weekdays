package com.github.streamlang.weekdays.extra.struct;

public class Pair<T> {
	private T left;
	private T right;
	
	public T getLeft() {
		return left;
	}
	
	public void setLeft(T left) {
		this.left = left;
	}
	
	public T getRight() {
		return right;
	}
	
	public void setRight(T right) {
		this.right = right;
	}
}
